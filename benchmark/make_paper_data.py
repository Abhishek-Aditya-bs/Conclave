#!/usr/bin/env python3
"""Turn the captured benchmark JSON into LaTeX-includable artifacts.

Emits, under paper/data/:
  * numbers.tex          — \newcommand macros for every figure quoted in prose/tables
  * <domain>_hist.dat    — split (clean vs attack) score histogram for pgfplots
  * latency.dat          — per-domain end-to-end latency percentiles

Run after capture.py has written benchmark/results/{fraud,security}-serving.json.
"""
from __future__ import annotations

import json
import os

RES = "benchmark/results"
OUT = "paper/data"
TAG = "serving"


def load(domain: str) -> dict:
    with open(f"{RES}/{domain}-{TAG}.json") as f:
        return json.load(f)


def pctnum(x: float, d: int = 1) -> str:
    return f"{100*x:.{d}f}"


def main() -> None:
    os.makedirs(OUT, exist_ok=True)
    fr = load("fraud")
    se = load("security")

    m: dict[str, str] = {}

    def add(name: str, val) -> None:
        m[name] = str(val)

    add("judgemodel", r"google/gemini-3.1-flash-lite")
    add("judgeendpoint", "OpenRouter")

    for tag, r in (("fraud", fr), ("sec", se)):
        det = r["detection"]
        cb = det["confusion_block"]
        cbr = det["confusion_block_or_review"]
        p1 = det["precision_recall_at_fpr_1pct"]
        sep = det["score_separation"]
        add(f"{tag}ndec", r["n_decisions"])
        add(f"{tag}auc", f"{det['roc_auc']:.3f}")
        # BLOCK operating point
        add(f"{tag}blockprec", pctnum(cb["precision"]))
        add(f"{tag}blockrec", pctnum(cb["recall"]))
        add(f"{tag}blockf", f"{cb['f1']:.3f}")
        add(f"{tag}blockfpr", pctnum(cb["fpr"], 1))
        add(f"{tag}blocktp", cb["tp"])
        add(f"{tag}blockfp", cb["fp"])
        add(f"{tag}blockfn", cb["fn"])
        add(f"{tag}blocktn", cb["tn"])
        # BLOCK+REVIEW operating point
        add(f"{tag}flagprec", pctnum(cbr["precision"]))
        add(f"{tag}flagrec", pctnum(cbr["recall"]))
        add(f"{tag}flagfpr", pctnum(cbr["fpr"], 1))
        # precision@FPR<=1% (letters-only macro names: LaTeX forbids digits)
        if p1:
            add(f"{tag}poneprec", pctnum(p1["precision"]))
            add(f"{tag}ponerec", pctnum(p1["recall"]))
            add(f"{tag}ponethr", f"{p1['threshold']:.2f}")
        # score separation
        add(f"{tag}cleanmean", f"{sep['clean_mean']:.2f}")
        add(f"{tag}attackmean", f"{sep['attack_mean']:.2f}")
        # latency
        lat = r["latency_ms"]
        add(f"{tag}latpfifty", f"{lat['p50']:.0f}")
        add(f"{tag}latpninefive", f"{lat['p95']:.0f}")
        add(f"{tag}latpninenine", f"{lat['p99']:.0f}")
        add(f"{tag}latmax", f"{lat['max']:.0f}")
        # verdict mix
        vm = r["verdict_mix"]
        for v in ("ALLOW", "REVIEW", "BLOCK"):
            add(f"{tag}mix{v.lower()}", vm.get(v, 0))
        # truth mix total attacks
        tm = r["truth_mix"]
        attacks = sum(c for k, c in tm.items() if k != "CLEAN")
        add(f"{tag}clean", tm.get("CLEAN", 0))
        add(f"{tag}attacks", attacks)

    # per attack-type recall
    for tag, r in (("fraud", fr), ("sec", se)):
        for lab, d in r["detection"]["per_attack_type_recall"].items():
            key = lab.lower().replace("_", "")
            add(f"{tag}rec{key}", pctnum(d["recall"], 0))
            add(f"{tag}n{key}", d["total"])

    # write numbers.tex
    with open(f"{OUT}/numbers.tex", "w") as f:
        f.write("% Auto-generated by benchmark/make_paper_data.py — do not edit by hand.\n")
        for k in sorted(m):
            f.write(f"\\newcommand{{\\{k}}}{{{m[k]}}}\n")

    # histograms (split clean/attack), bin center 0.05..0.95
    for tag, r in (("fraud", fr), ("security", se)):
        h = r["score_hist_split"]
        edges = h["edges"]
        centers = [(edges[i] + edges[i + 1]) / 2 for i in range(len(edges) - 1)]
        with open(f"{OUT}/{tag}_hist.dat", "w") as f:
            f.write("center clean attack\n")
            for c, cl, at in zip(centers, h["clean"], h["attack"]):
                f.write(f"{c:.2f} {cl} {at}\n")

    # latency percentiles dat
    with open(f"{OUT}/latency.dat", "w") as f:
        f.write("domain p50 p95 p99\n")
        for tag, r in (("fraud", fr), ("security", se)):
            lat = r["latency_ms"]
            f.write(f"{tag} {lat['p50']:.0f} {lat['p95']:.0f} {lat['p99']:.0f}\n")

    print("wrote", f"{OUT}/numbers.tex", "+ histograms + latency.dat")
    print(f"  macros: {len(m)}")


if __name__ == "__main__":
    main()
